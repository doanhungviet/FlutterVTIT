void main() {
  dynamic x; // Biến x có kiểu dữ liệu dynamic

  x = 10; // Gán giá trị kiểu integer cho biến x
  print('Giá trị của x là: $x');

  x = 'Hello'; // Gán giá trị kiểu string cho biến x
  print('Giá trị của x là: $x');


  var a = 10; // Biến a có kiểu dữ liệu được gán tự động là integer
  var b = 'Hello'; // Biến b có kiểu dữ liệu được gán tự động là string


  print('Giá trị của a là: $a');
  print('Giá trị của b là: $b');
}